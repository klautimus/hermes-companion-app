     * Expand all emoji shortcodes in the input text.
     * Known shortcodes are replaced with Unicode emoji.
     * Unknown shortcodes are left as-is.
     */
    fun expand(text: String): String {
        return SHORTCODE_REGEX.replace(text) { matchResult ->
            val shortcode = matchResult.groupValues[1]
            EMOJI_MAP[shortcode] ?: matchResult.value
        }
    }
